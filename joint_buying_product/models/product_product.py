from odoo import api, fields, models


class ProductProduct(models.Model):
    _inherit = ["product.product", "joint.buying.mixin"]
    _name = "product.product"

    is_joint_buying = fields.Boolean(
        string="For Joint Buying",
        related="product_tmpl_id.is_joint_buying",
        store=True,
        readonly=True,
    )

    @api.model
    def _search(
        self,
        args,
        offset=0,
        limit=None,
        order=None,
        count=False,
        access_rights_uid=None,
    ):
        res_partner_id = self.env.context.get("search_default_seller_ids", False)
        if res_partner_id:
            args += [("seller_ids.name", "in", res_partner_id)]
        return super()._search(
            args=args,
            offset=offset,
            limit=limit,
            order=order,
            count=count,
            access_rights_uid=access_rights_uid,
        )
